/// # 应用导航分区枚举
///
/// 应用导航分区枚举定义，负责统一页面分区语义。
///
/// ## 主要功能
/// - 提供 cards、pool 两个可切换分区标识
/// - 作为导航状态在业务中引用的标准枚举值
///
/// ## 分区说明
/// - [cards] - 卡片管理分区，用于卡片列表和操作
/// - [pool] - 数据池分区，用于网络数据池相关功能
///
/// ## 使用示例
/// ```dart
/// final section = AppSection.cards;
/// if (section == AppSection.pool) {
///   // 处理数据池逻辑
/// }
/// ```
library app_section;

/// 应用导航分区枚举。
///
/// 定义应用中可用的主要导航分区。
/// 每个分区对应一个主要功能模块的入口。
enum AppSection {
  /// 卡片管理分区。
  ///
  /// 用于卡片列表展示、卡片编辑、卡片操作等功能。
  cards,

  /// 数据池分区。
  ///
  /// 用于网络数据池相关功能，包括加入数据池、数据同步等。
  pool,
}
